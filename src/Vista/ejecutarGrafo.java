    private void graficarVertice(Graphics grafo, String dato, int x, int y){
        //graficando
        if(dato.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un dato");
        }else{
            grafo.fillOval(x, y, 60, 30);
            grafo.setColor(Color.WHITE);
            grafo.drawString(dato, x+15, y+15);
        }
    }
    private void graficarArista(Graphics grafo, int x, int y, int x1, int y1, int peso){
        grafo.drawLine(x+30, y+15, x1, y1);
        grafo.setColor(Color.BLACK);
        grafo.drawString(String.valueOf(peso), (x1+x)/2 , (y1+y)/2);
    }
    private void graficarDestino(Graphics grafo, int x, int y){
        grafo.setColor(Color.yellow);
        grafo.fillOval(x, y, 10, 10);
    }
    
