    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        try{
            String origen=cbxOrigen.getSelectedItem().toString();
            String destino=cbxDestino.getSelectedItem().toString();
            int peso=Integer.parseInt(txtPeso.getText());
            
            //tansformar
            Vertice o=grafoCiudades.buscarVertice(origen);
            Vertice d=grafoCiudades.buscarVertice(destino);
            
            o.agregarArista(new Arista(o,d,peso));
            //graficamos arista
            graficarArista(pnlGrafo.getGraphics(), o.x, o.y, d.x, d.y, peso);
            graficarDestino(pnlGrafo.getGraphics(), d.x, d.y);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error");
        }
        txtPeso.setText("");
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void pnlGrafoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlGrafoMouseClicked
        // TODO add your handling code here:
        try{
            int x=evt.getX();
            int y=evt.getY();
            Graphics grafo=this.pnlGrafo.getGraphics();
            String nombreCiudad = JOptionPane.showInputDialog("Ingrese la ciudad: ");
            
            graficarVertice(grafo, nombreCiudad, x, y);
            
            Vertice nuevoVertice=new Vertice(nombreCiudad, x, y);
            grafoCiudades.agregarVertice(nuevoVertice);
            cbxOrigen.addItem(nombreCiudad);
            cbxDestino.addItem(nombreCiudad);
                    
                    
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_pnlGrafoMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String ciudad=txtBuscar.getText();
        String msg="";
        Vertice resp = grafoCiudades.buscarVertice(ciudad);

        if (resp != null) {
            ArrayList<Arista> adyacentes = resp.mostrarAristasAdyacentes();
            for (Arista a : adyacentes) {
                msg+="=>" + a.destino.cuidad + " con peso " + a.peso+"\n";
            }
            JOptionPane.showMessageDialog(null, "La ciudad '"+resp.cuidad+"' encontrada!!"+
                    "\nPosibles Destinos:\n"+
                    msg);
        } else {
            JOptionPane.showMessageDialog(null, "No existe!!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

